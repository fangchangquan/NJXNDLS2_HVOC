void Temperature_Data_Process(void)
{
	  static uint temp_cnt=0;
	  		   
	   if(temp_cnt<TMP_COUNT)
	   {
		    temp_group[temp_cnt]=t_temp;
			  temp_cnt++;
		 }
		 else if(temp_cnt>=TMP_COUNT)
		 {
			  
			  sensor.temp_now=(s16)Filter_Temp(temp_group,TMP_COUNT);

			  temp_cnt=0;
		 }
     		 
		  sensor.tem.temperature=sensor.temp_now;
}		 

